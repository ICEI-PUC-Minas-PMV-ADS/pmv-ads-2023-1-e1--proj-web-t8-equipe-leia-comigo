// Construindo funcao de movimentacao do carrossel presents

const sliders_presents = document.getElementById("present");

var scrollPerClick_presents;
var imagePadding_presents = 20;
var scrollAmount_presents = 0;

function sliderScrollLeft_presents() {
  sliders_presents.scrollTo({
    top: 0,
    left: (scrollAmount_presents -= scrollPerClick_presents),
    behavior: "smooth",
  });

  if (scrollAmount_presents < 0) {
    scrollAmount_presents = 0;
  }
}

function sliderScrollRight_presents() {
  if (
    scrollAmount_presents <=
    sliders_presents.scrollWidth - sliders_presents.clientWidth
  ) {
    sliders_presents.scrollTo({
      top: 0,
      left: (scrollAmount_presents += scrollPerClick_presents),
      behavior: "smooth",
    });
  }
}

scrollPerClick_presents =
  document.querySelector(".img-presents_0").clientWidth + imagePadding_presents;

